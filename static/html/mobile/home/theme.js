	function generateSearch(file_list_tree) {

		function displayResults(res) {

			var new_list = [];
			for (var i = 0; i < file_list_tree.length; i++) {

				var this_file = file_list_tree[i];
				for (var n = 0; n < res.length; n++) {
					var this_res = res[n];
					if (this_file.name == this_res.title) new_list.push(this_file)
				}
			}

			createFileList(new_list);

		}

		
		var file_content = [];
		for (var i = 0; i < file_list_tree.length; i++) {
			var this_file = file_list_tree[i];
			file_content.push({
				title: this_file.name
			})
		}

		$('#file-search').search({
			source: file_content,
			searchFields: [
				'title'
			],
			fullTextSearch: false,
			onResults: function (response) {
				displayResults(response.results);
			}
		});

		$('#file-search input').keyup(function() {
			var search_str = jQuery(this).val();
			if (search_str == "" || search_str == " ") createFileList(file_list_tree);
		})
	}


	function createFileList(file_list_tree) {

		var html = "";

		//sorting
		var new_list = [];
		for (var i = 0; i < file_list_tree.length; i++) {
			var this_file = file_list_tree[i];
			if (this_file.type == "folder") new_list.push(this_file);
		}

		for (var i = 0; i < file_list_tree.length; i++) {
			var this_file = file_list_tree[i];
			if (this_file.type != "folder") new_list.push(this_file);
		}


		for (var i = 0; i < new_list.length; i++) {

			var this_file = new_list[i];
			var this_thumbnail_url = this_file.thumbnail;
			if (this_file.type == "folder") this_thumbnail_url = "https://platform.skyciv.com/storage/images/dashboard/fileman.png";

			html+= '<div class="item" file-id="'+i+'">';
				html += '<a class="ui mini image">';
					html += '<img src="' + this_thumbnail_url+'">';
				html += '</a>';
				html += '<div class="content">';
					html += '<a class="header">'+this_file.name+'</a>';
					html += '<div class="description">';
						html += '<p>Open in Viewer</p>';
					html += '</div>';
				html += '</div>';
			html += '</div>';

		}

		current_file_list_tree = new_list.slice();

		jQuery(".file-list").html(html);

		jQuery("#fileman-file-list .item").unbind().click(function () {
			var this_id = jQuery(this).attr("file-id");
			var this_file = current_file_list_tree[this_id];
			if (this_file.type == "folder") {
				createFileList(this_file.contents);
			} else {
				//open file
				alert('open file')
			}

		});


	}


